package org.openXpertya.print.fiscal.hasar.pojos;

public class CierreDiarioInformacionDocumento { 
	public int Orden;
	public int PrimerComprobante;
	public int UltimoComprobante;
	public int CantidadEmitidos;
	public int CantidadCancelados;
	public int getOrden() {
		return Orden;
	}
	public void setOrden(int orden) {
		Orden = orden;
	}
	public int getPrimerComprobante() {
		return PrimerComprobante;
	}
	public void setPrimerComprobante(int primerComprobante) {
		PrimerComprobante = primerComprobante;
	}
	public int getUltimoComprobante() {
		return UltimoComprobante;
	}
	public void setUltimoComprobante(int ultimoComprobante) {
		UltimoComprobante = ultimoComprobante;
	}
	public int getCantidadEmitidos() {
		return CantidadEmitidos;
	}
	public void setCantidadEmitidos(int cantidadEmitidos) {
		CantidadEmitidos = cantidadEmitidos;
	}
	public int getCantidadCancelados() {
		return CantidadCancelados;
	}
	public void setCantidadCancelados(int cantidadCancelados) {
		CantidadCancelados = cantidadCancelados;
	}
	
	
}
