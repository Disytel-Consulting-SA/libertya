package org.openXpertya.print.fiscal.hasar.pojos;

public class CierreDiarioTotales { 
	public int Orden;
	public double TotalGravado;
	public int TotalNoGravado;
	public int TotalExento;
	public double TotalIVA;
	public int TotalOtrosTributos;
	public int TotalBonificaciones;
	public double TotalFinal;
	public int getOrden() {
		return Orden;
	}
	public void setOrden(int orden) {
		Orden = orden;
	}
	public double getTotalGravado() {
		return TotalGravado;
	}
	public void setTotalGravado(double totalGravado) {
		TotalGravado = totalGravado;
	}
	public int getTotalNoGravado() {
		return TotalNoGravado;
	}
	public void setTotalNoGravado(int totalNoGravado) {
		TotalNoGravado = totalNoGravado;
	}
	public int getTotalExento() {
		return TotalExento;
	}
	public void setTotalExento(int totalExento) {
		TotalExento = totalExento;
	}
	public double getTotalIVA() {
		return TotalIVA;
	}
	public void setTotalIVA(double totalIVA) {
		TotalIVA = totalIVA;
	}
	public int getTotalOtrosTributos() {
		return TotalOtrosTributos;
	}
	public void setTotalOtrosTributos(int totalOtrosTributos) {
		TotalOtrosTributos = totalOtrosTributos;
	}
	public int getTotalBonificaciones() {
		return TotalBonificaciones;
	}
	public void setTotalBonificaciones(int totalBonificaciones) {
		TotalBonificaciones = totalBonificaciones;
	}
	public double getTotalFinal() {
		return TotalFinal;
	}
	public void setTotalFinal(double totalFinal) {
		TotalFinal = totalFinal;
	}
	
	
}
