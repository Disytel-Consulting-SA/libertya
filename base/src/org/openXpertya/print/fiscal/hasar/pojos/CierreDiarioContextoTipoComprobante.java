package org.openXpertya.print.fiscal.hasar.pojos;

public class CierreDiarioContextoTipoComprobante { 
	public int Orden;
	public String TipoComprobante;
	public String SubTipoComprobante;
	public String CalificadorComprobante;
	public String Estacion;
	public String ConLeyenda;
	public int getOrden() {
		return Orden;
	}
	public void setOrden(int orden) {
		Orden = orden;
	}
	public String getTipoComprobante() {
		return TipoComprobante;
	}
	public void setTipoComprobante(String tipoComprobante) {
		TipoComprobante = tipoComprobante;
	}
	public String getSubTipoComprobante() {
		return SubTipoComprobante;
	}
	public void setSubTipoComprobante(String subTipoComprobante) {
		SubTipoComprobante = subTipoComprobante;
	}
	public String getCalificadorComprobante() {
		return CalificadorComprobante;
	}
	public void setCalificadorComprobante(String calificadorComprobante) {
		CalificadorComprobante = calificadorComprobante;
	}
	public String getEstacion() {
		return Estacion;
	}
	public void setEstacion(String estacion) {
		Estacion = estacion;
	}
	public String getConLeyenda() {
		return ConLeyenda;
	}
	public void setConLeyenda(String conLeyenda) {
		ConLeyenda = conLeyenda;
	}
	
	
}
