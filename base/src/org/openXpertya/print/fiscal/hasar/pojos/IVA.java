	public double MontoIVA;
	public int getTasaIVA() {
		return TasaIVA;
	}
	public void setTasaIVA(int tasaIVA) {
		TasaIVA = tasaIVA;
	}
	public int getImpIntFijo() {
		return ImpIntFijo;
	}
	public void setImpIntFijo(int impIntFijo) {
		ImpIntFijo = impIntFijo;
	}
	public int getImpIntVariable() {
		return ImpIntVariable;
	}
	public void setImpIntVariable(int impIntVariable) {
		ImpIntVariable = impIntVariable;
	}
	public double getNetoSinIVA() {
		return NetoSinIVA;
	}
