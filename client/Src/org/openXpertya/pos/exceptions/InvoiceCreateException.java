package org.openXpertya.pos.exceptions;

public class InvoiceCreateException extends PosException {

	public InvoiceCreateException() {
		super();
	}

	public InvoiceCreateException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public InvoiceCreateException(String arg0) {
		super(arg0);
	}

	public InvoiceCreateException(Throwable arg0) {
		super(arg0);
	}
}
